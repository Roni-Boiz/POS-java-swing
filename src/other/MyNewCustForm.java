            sql="update registration set regID=?,regDate=? where regID=?";
            pstm1 = connection.prepareStatement(sql);
            pstm1.setObject(1, txtCustRegID.getText());
            pstm1.setObject(2, txtCustRegDate.getText());
            pstm1.setObject(3, txtCustRegID.getText());
            
            pstm1.executeUpdate();

