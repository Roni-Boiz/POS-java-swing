    private void btnCustRegActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCustRegActionPerformed

        // TODO add your handling code here:
        
        try {

            String custID = txtcustID.getText();
            String custName = txtCustName.getText();
            String custAddress = txtCustAddress.getText();
            int custNum = Integer.parseInt(txtCustNum.getText());
            String custMail = txtCustMail.getText();
            String regID = txtCustRegID.getText();
            String regDate = txtCustRegDate.getText();

            
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/mycustform", "root", "cmjd49");

            
            

            String sql="insert into registration values (?,?)";
            PreparedStatement pstm1 = connection.prepareStatement(sql);
            
            
            pstm1.setObject(1, regID);
            pstm1.setObject(2, regDate);

            int i = pstm1.executeUpdate();
            
            sql = "insert into customer values(?,?,?,?,?,?)";
            PreparedStatement pstm2 = connection.prepareStatement(sql);
            
            pstm2.setObject(1,custID);
            pstm2.setObject(2, regID);
            pstm2.setObject(3,custName);
            pstm2.setObject(4,custAddress);
            pstm2.setObject(5,custNum);
            pstm2.setObject(6,custMail);
            
            i = pstm2.executeUpdate();
            
            
            if (i>0) {
                loadAllCustomers();
                JOptionPane.showMessageDialog(this, "Customer is registered", "Done", JOptionPane.INFORMATION_MESSAGE);
                removeAllText();
                txtcustID.requestFocus();
            } else{
                JOptionPane.showMessageDialog(this, "Customer is not registered", "Not Done", JOptionPane.ERROR_MESSAGE);
            }


            

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Warning", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Warning", JOptionPane.ERROR_MESSAGE);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(MyNewCustForm.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnCustRegActionPerformed

